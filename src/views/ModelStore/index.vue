<template>
  <div id="ModelStore">
    <div id="Selection">
      <selection
        title="项目"
        :list="projectList"
      ></selection>
      <selection
        title="后缀名"
        :list="fileSuffixList"
      ></selection>
    </div>
    <div id="DataTable">
      <bk-table
        style="margin-top: 15px"
        :data="data"
        :size="'small'"
        :pagination="pagination"
        @page-change="handlePageChange"
        @page-limit-change="handlePageLimitChange"
      >
        <bk-table-column
          type="index"
          label="ID"
          width="60"
        ></bk-table-column>
        <bk-table-column
          label="文件名"
          prop="fileName"
        ></bk-table-column>
        <bk-table-column
          label="来源"
          prop="source"
        ></bk-table-column>
        <bk-table-column
          label="状态"
          prop="status"
        ></bk-table-column>
        <bk-table-column
          label="创建时间"
          prop="createTime"
        ></bk-table-column>
        <bk-table-column
          label="操作"
          width="150"
        >
          <template slot-scope="props">
            <bk-button
              class="mr10"
              theme="primary"
              text
              :disabled="props.row.status === '上传中'"
              @click="reset(props.row)"
            >下载
            </bk-button>
            <bk-button
              class="mr10"
              theme="primary"
              text
              @click="remove(props.row)"
            >删除</bk-button>
          </template>
        </bk-table-column>
      </bk-table>
    </div>
    <div style="margin-top: 40px; width: 60%; display: inline-block">
      <bk-divider align="center">上传模型</bk-divider>
    </div>
    <div id="UploadModel">
      <bk-upload
        :tip="'最多上传10个文件'"
        :limit="uploadLimit"
        :with-credentials="true"
        :handle-res-code="handleRes"
        :url="'http://localhost:8000/api/uploadModel'"
        @on-success="uploadSuccess"
        @on-progress="uploadProgress"
        @on-done="uploadDone"
        @on-error="uploadErr"
      ></bk-upload>
    </div>
  </div>
</template>

<script>
import axios from 'axios'
import selection from '@/components/Selection'
import { bkTable, bkTableColumn, bkButton, bkUpload, bkDivider } from 'bk-magic-vue'

export default {
  name: 'ModelStore',
  components: {
    selection,
    bkTable,
    bkTableColumn,
    bkButton,
    bkUpload,
    bkDivider
  },
  created () {
    // get model store information
    axios.get('http://localhost:8000/api/getModelStoreInfo').then(res => {
      this.data = res.data.modelStoreInfo
      this.projectList = res.data.projectList
      this.fileSuffixList = res.data.fileSuffixList
    })
  },
  data () {
    return {
      projectList: [
        {
          id: 0,
          name: 'Sample Project'
        }
      ],
      fileSuffixList: [
        {
          id: 0,
          name: 'Sample file suffix'
        }
      ],
      size: 'small',
      data: [
        {
          fileName: 'Sample model',
          source: '用户上传',
          status: '上传中',
          createTime: '2021-01-25 15:02:24'
        }
      ],
      pagination: {
        current: 1,
        count: 3,
        limit: 20
      },
      uploadLimit: 10
    }
  },
  methods: {
    handlePageLimitChange () {
      console.log('handlePageLimitChange', arguments)
    },
    handlePageChange (page) {
      this.pagination.current = page
    },
    uploadSuccess (file, fileList) {
      console.log(file, fileList, 'success')
    },
    uploadProgress (e, file, fileList) {
      console.log(e, file, fileList, 'progress')
    },
    uploadDone () {
      console.log('done')
    },
    uploadErr (file, fileList) {
      console.log(file, fileList, 'error')
    },
    handleRes (response) {
      // 在这里处理返回结果，目前固定为成功
      return true
    }
  }
}
</script>

